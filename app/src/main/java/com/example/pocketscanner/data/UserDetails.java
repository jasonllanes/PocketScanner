package com.example.pocketscanner.data;

public class UserDetails {

    public String id;
    public String name;

    public String email;
    public String password;

    public String profilePic;

    public UserDetails(){ }
    public UserDetails(String id,String name, String email, String password, String profilePic) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = password;
        this.profilePic = profilePic;
    }









}
